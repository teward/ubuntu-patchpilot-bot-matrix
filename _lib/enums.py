from enum import Enum


class ReactionEmojis(Enum):
    WARNING = "\u26A0"  # \u26A0 is the warning sign
    CHECK_MARK = "\u2705"  # \u2705 is the White Heavy Check Mark
    CROSS_MARK = "\u274C"  # \u274C is the red X / "Cross Mark"
    QUESTION_MARK = "\u2705"  # \u2753 is the question mark
    BOOM = "\uD83D\uDCA5"
